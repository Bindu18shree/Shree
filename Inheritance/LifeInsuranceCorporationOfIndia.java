package com.xworkz.lic.boot;

public class LifeInsuranceCorporationOfIndia {
	
	public String locationOfHeadQuarters;
	
	public String owner;
	
	public int foundedYear;
	
	public long noOfEmployees;


	public String getLocationOfHeadQuarters() {
		return locationOfHeadQuarters;
	}

	public String getOwner() {
		return owner;
	}

	public int getFoundedYear() {
		return foundedYear;
	}

	public long getNoOfEmployees() {
		return noOfEmployees;
	}

	public void setLocationOfHeadQuarters(String locationOfHeadQuarters) {
		this.locationOfHeadQuarters = locationOfHeadQuarters;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}

	public void setFoundedYear(int foundedYear) {
		this.foundedYear = foundedYear;
	}

	public void setNoOfEmployees(long noOfEmployees) {
		this.noOfEmployees = noOfEmployees;
	}
}
