class PeriodicElements{
	static  int atomicNo=5;
	static int number=6;
	static int atomicNumber=7;
	static int atomicNum=8;
	static int period=2;
	static char symbolOfBoron='B';
	static char symbolOfCarbon='C';
	static char symbolOfNitrogen = 'N';
	static char symbolOfOxygen = 'O';
	static char valenceshell = 'L';
	
	public static void main(String[] args){
		System.out.println("Atomic Number of Boron is ="+atomicNo);
		System.out.println("Atomic Number of Carbon is ="+number);
		System.out.println("Atomic Number of Nitrogen is ="+atomicNumber);
		System.out.println("Atomic Number of Oxygen is ="+atomicNum);
		System.out.println("All these Elements are in the same period ="+period);
		System.out.println("Symbol of the element Boron is ="+symbolOfBoron);
		System.out.println("Symbol of the element Carbon is ="+symbolOfCarbon);
		System.out.println("Symbol of the element Nitrogen is ="+symbolOfNitrogen);
		System.out.println("Symbol of the element Oxygen is ="+symbolOfOxygen);
		System.out.println("Last electrons of all the elements are in the valence shell ="+valenceshell);
	}
}