package com.xworkz.ecommerceaaplications.boot;

public class Ajio {
	
	public String ownedBy;
	
	public int foundedIn;
	
	public double noOfBrandsAvailable;
	
	public String headQuarters;
	
	

	public String getOwnedBy() {
		return ownedBy;
	}

	public int getFoundedIn() {
		return foundedIn;
	}

	public double getNoOfBrandsAvailable() {
		return noOfBrandsAvailable;
	}

	public String getHeadQuarters() {
		return headQuarters;
	}

	public void setOwnedBy(String ownedBy) {
		this.ownedBy = ownedBy;
	}

	public void setFoundedIn(int foundedIn) {
		this.foundedIn = foundedIn;
	}

	public void setNoOfBrandsAvailable(double noOfBrandsAvailable) {
		this.noOfBrandsAvailable = noOfBrandsAvailable;
	}

	public void setHeadQuarters(String headQuarters) {
		this.headQuarters = headQuarters;
	}
}
