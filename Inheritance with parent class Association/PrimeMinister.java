package com.xworkz.politics.boot;

public class PrimeMinister {
	
	public String name;
	
	public int age;
	
	public float numberOfYearsPassedStartedAsPM;

	
	
	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public float getNumberOfYearsPassedStartedAsPM() {
		return numberOfYearsPassedStartedAsPM;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public void setNumberOfYearsPassedStartedAsPM(float numberOfYearsPassedStartedAsPM) {
		this.numberOfYearsPassedStartedAsPM = numberOfYearsPassedStartedAsPM;
	}
	
	
	

}
