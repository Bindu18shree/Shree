package com.xworkz.students.runner;

public class CastingRunner {
	
	public static void main(String [] args) {
		
		// Conversion of int to other primitive data types 
		int number = 117;
		byte conversionOfIntToByte = (byte)number;
		System.out.println("Conversion of int to byte :" +conversionOfIntToByte);
		
		char conversionOfIntToChar =(char) number;
		System.out.println("Conversion of int to char :" +conversionOfIntToChar);
		
		long conversionOfIntToLong = number;
		System.out.println("Conversion of int to long :" +conversionOfIntToLong);
		
		short conversionOfIntToShort =(short) number;
		System.out.println("Conversion of int to short :" +conversionOfIntToShort);
		
	    float conversionOfIntToFloat = number;
	    System.out.println("Conversion of int to float :" +conversionOfIntToFloat);
	    
	    double conversionOfIntToDouble = number;
	    System.out.println("Conversion of int to double :" +conversionOfIntToDouble);
		
		
	    // Conversion of char to other primitive data types
	    char character = 'B';
	    int conversionOfCharToInt = character;
	    System.out.println("Conversion of char to int :" +conversionOfCharToInt);
	    
		byte conversionOfCharToByte =(byte) character;
		System.out.println("Conversion of char to byte :" +conversionOfCharToByte);
		
		short conversionOfCharToShort = (short) character;
		System.out.println("Conversion of char to short :" +conversionOfCharToShort);
		
		long conversionOfCharToLong = character;
		System.out.println("Conversion of char to long :" +conversionOfCharToLong);
		
		float conversionOfCharToFloat = character;
		System.out.println("Conversion of char to float :" +conversionOfCharToFloat);
		
		double conversionOfCharToDouble = character;
		System.out.println("Conversion of char to double :" +conversionOfCharToDouble);
		
		
		// Conversion of boolean to other primitive data types
		boolean yesOrNo = true;
		int conversionOfBooleanToInt = yesOrNo ? 1:0;
		System.out.println("Conversion of boolean to int :" +conversionOfBooleanToInt);
		
		char conversionOfBoolenToChar = yesOrNo ? 'Y':'N';
		System.out.println("Conversion of boolean to char :" +conversionOfBoolenToChar);
		
		long conversionOfBooleanToLong = yesOrNo ? 2:4;
		System.out.println("Conversion of boolean to long :" +conversionOfBooleanToLong);
		
	
		// Conversion of byte to other primitive data types
		byte num = 127;
		int conversionOfByteToInt = num;
		System.out.println("Conversion of byte to int :" +conversionOfByteToInt);
		
		char conversionOfByteToChar = (char) num;
		System.out.println("Conversion of byte to char :" +conversionOfByteToChar);
		
		float conversionOfByteToFloat = num;
		System.out.println("Conversion of byte to float :" +conversionOfByteToFloat);
		
		double conversionOfByteToDouble = num;
		System.out.println("Conversion of byte to double :" +conversionOfByteToDouble);
		
		short conversionOfByteToShort = num;
		System.out.println("Conversion of byte to short :" +conversionOfByteToShort);
		
		long conversionOfByteToLong = num;
		System.out.println("Conversion of byte to long :" +conversionOfByteToLong);
		
		
		// Conversion of float to other primitive data types
		float digit = 62.3874f;
		int conversionOfFloatToInt = (int) digit;
		System.out.println("Conversion of float to int :" +conversionOfFloatToInt);
		
		short conversionOfFloatToShort = (short) digit;
		System.out.println("Conversion of float to short :" +conversionOfFloatToShort);
		
		byte conversionOfFloatToByte = (byte) digit;
		System.out.println("Conversion of float to byte :" +conversionOfFloatToByte);
		
		char conversionOfFloatToChar = (char) digit;
		System.out.println("Conversion of float to char :" +conversionOfFloatToChar);
		
		double conversionOfFloatToDouble = digit;
		System.out.println("Conversion of float to double :" +conversionOfFloatToDouble);
		
		long conversionOfFloatToLong = (long) digit;
		System.out.println("Conversion of float to long :" +conversionOfFloatToLong);
			
		
		// Conversion of double to other primitive data types
		double digits = 205.356478d;
		int conversionOfDoubleToInt = (int) digits;
		System.out.println("Conversion of double to int :" +conversionOfDoubleToInt);
		
		float conversionOfDoubleToFloat =(float) digits;
		System.out.println("Conversion of double to float :" +conversionOfDoubleToFloat);
		
		short conversionOfDoubleToShort = (short) digits;
		System.out.println("Conversion of double to short :" +conversionOfDoubleToShort);
		
		byte conversionOfDoubleToByte = (byte) digits;
		System.out.println("Conversion of double to byte :" +conversionOfDoubleToByte);
		
		char conversionOfDoubleToChar = (char) digits;
		System.out.println("Conversion of double to char :" +conversionOfDoubleToChar);
		
		long conversionOfDoubleToLong = (long) digits;
		System.out.println("Conversion of double to long :" +conversionOfDoubleToLong);
		
		
		
	}

}
