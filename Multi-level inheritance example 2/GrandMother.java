package com.xworkz.family.boot;

public class GrandMother {
	
	public String name;
	public int age;
	public float height;
	public double weight;
	public char skinColor;
	public boolean isGrandMotherRetiredOrNot;
	
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public float getHeight() {
		return height;
	}
	public double getWeight() {
		return weight;
	}
	public char getSkinColor() {
		return skinColor;
	}
	public boolean isGrandMotherRetiredOrNot() {
		return isGrandMotherRetiredOrNot;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public void setHeight(float height) {
		this.height = height;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public void setSkinColor(char skinColor) {
		this.skinColor = skinColor;
	}
	public void setIsGrandMotherRetiredOrNot(boolean isGrandMotherRetiredOrNot) {
		this.isGrandMotherRetiredOrNot = isGrandMotherRetiredOrNot;
	}
	
	

}
